package githubCommands;

public class githubBranchCheck {
    /*
        Projedeki değişiklikleri uzak depoyla (GitHub) senkronize etmek için kullanılan
        temel 'push' (gönderme) ve 'pull' (çekme) işlemleri için en basit komutlar aşağıdadır.

        1. Değişiklikleri Uzak Depodan Çekme (git pull):
           Başkalarının yaptığı ve uzak depoya gönderdiği değişiklikleri kendi yerel makinenize almak için kullanılır.
           Çalışmaya başlamadan önce projenin en güncel halini almak için bu komutu kullanmak iyi bir alışkanlıktır.
           git pull origin <branch_adi>

           Örnek (main branch'indeki değişiklikleri çekmek için):
           git pull origin main

        2. Değişiklikleri Uzak Depoya Gönderme (git push):
           Kendi yerel makinenizde 'commit'lediğiniz (kaydettiğiniz) değişiklikleri uzak depoya göndermek için kullanılır.
           Böylece diğer ekip üyeleri de sizin değişikliklerinizi alabilir.
           git push origin <branch_adi>

           Örnek (üzerinde çalıştığınız özelliği göndermek için):
           git push origin feature/kullanici-kayit
     */
}
